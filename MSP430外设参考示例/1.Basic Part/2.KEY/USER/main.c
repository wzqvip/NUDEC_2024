#include "A_include.h"
/********************************************************************************************************************
*                  ------------------------------------
LEDS      A0
LEDR      B26
LEDG      B27
LEDB      B22
KEY0      A18
KEY1      B21
*                  ------------------------------------
********************************************************************************************************************/

/*按键控制LED*闪烁*/
int main(void)
{
	SYSCFG_DL_init(); //芯片资源初始化,由SysConfig配置软件自动生成
    KEY_Init();
	while(1)
	{
        
        if(KEY0)LEDR(1);
        else LEDR(0);
        if(!KEY1)LEDG(1);
        else LEDG(0);
	}
}
